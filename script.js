let currentLang = localStorage.getItem('site_lang') || 'en';

// --- Initialization ---
document.addEventListener('DOMContentLoaded', () => {
    initTheme();
    initLanguage();
    initScrollAnimations();
    fetchProjects();
});

// --- Language Toggle Logic ---
function initLanguage() {
    const langSelect = document.getElementById('lang-select');
    
    // Set initial select value
    if (langSelect) {
        langSelect.value = currentLang;
        
        langSelect.addEventListener('change', (e) => {
            currentLang = e.target.value;
            localStorage.setItem('site_lang', currentLang);
            updateTranslations();
        });
    }

    updateTranslations();
}

function updateTranslations() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            // Update document title explicitly
            if (key === 'page_title') {
                document.title = translations[currentLang][key];
            } else {
                // Update text content
                el.textContent = translations[currentLang][key];
            }
        }
    });
}

// --- Dark Mode Logic ---
function initTheme() {
    const themeBtn = document.getElementById('theme-toggle');
    const sunIcon = document.querySelector('.sun-icon');
    const moonIcon = document.querySelector('.moon-icon');
    
    const savedTheme = localStorage.getItem('site_theme') || 'light';
    if (savedTheme === 'dark') {
        document.documentElement.setAttribute('data-theme', 'dark');
        sunIcon.style.display = 'none';
        moonIcon.style.display = 'block';
    }

    themeBtn.addEventListener('click', () => {
        const currentTheme = document.documentElement.getAttribute('data-theme');
        let targetTheme = 'light';
        
        if (currentTheme !== 'dark') {
            targetTheme = 'dark';
            document.documentElement.setAttribute('data-theme', 'dark');
            sunIcon.style.display = 'none';
            moonIcon.style.display = 'block';
        } else {
            document.documentElement.removeAttribute('data-theme');
            sunIcon.style.display = 'block';
            moonIcon.style.display = 'none';
        }
        
        localStorage.setItem('site_theme', targetTheme);
    });
}

// --- GitHub API Integration ---
async function fetchProjects() {
    const container = document.getElementById('projects-container');
    const loader = document.getElementById('projects-loader');
    
    try {
        const response = await fetch('https://api.github.com/users/HopeAgent007/repos?sort=updated');
        if (!response.ok) throw new Error('Failed to fetch repositories');
        
        const repos = await response.json();
        
        // Remove loader
        if (loader) loader.remove();
        
        // Filter out the website repo and the newly featured SDAHymns repo
        const filteredRepos = repos.filter(repo => 
            repo.name !== 'HopeAgent007-website' && 
            repo.name !== 'SDAHymns'
        );

        if (filteredRepos.length === 0) {
            // If no other projects, container can just be empty since we have "Coming Soon" card
            return;
        }

        filteredRepos.forEach(repo => {
            const card = document.createElement('div');
            card.className = 'project-card scroll-fade';
            
            // Format repo name (e.g. Live-Screens-SDA -> Live Screens SDA)
            const formattedName = repo.name.replace(/-/g, ' ');
            
            card.innerHTML = `
                <div class="project-icon">
                    <svg width="32" height="32" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="M22 19a2 2 0 0 1-2 2H4a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h5l2 3h9a2 2 0 0 1 2 2z"/></svg>
                </div>
                <h3 class="project-title">${formattedName}</h3>
                <p class="project-desc">${repo.description || 'A Seventh-day Adventist ministry tool.'}</p>
                <a href="${repo.html_url}" target="_blank" rel="noopener noreferrer" class="project-link">
                    <span data-i18n="project_view_github">${translations[currentLang]['project_view_github'] || 'View on GitHub'}</span>
                    <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="M5 12h14M12 5l7 7-7 7"/></svg>
                </a>
            `;
            container.appendChild(card);
        });

        // Re-trigger observer for newly added cards
        initScrollAnimations();
        
    } catch (error) {
        console.error('Error fetching projects:', error);
        if (loader) {
            loader.style.animation = 'none';
            loader.style.borderColor = 'transparent';
            loader.innerHTML = '<p style="color:var(--clr-text-light);">Failed to load projects. Please try again later.</p>';
        }
    }
}

// --- Scroll Animations ---
function initScrollAnimations() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('is-visible');
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.1, rootMargin: "0px 0px -50px 0px" });

    document.querySelectorAll('.scroll-fade').forEach(el => {
        observer.observe(el);
    });
}
